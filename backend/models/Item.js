const mongoose = require('mongoose');

const itemSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    image: {
        type: String // Path to the uploaded image
    }
}, {
    timestamps: true
});

module.exports = mongoose.model('Item', itemSchema);
